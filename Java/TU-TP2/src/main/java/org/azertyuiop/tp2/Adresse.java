package org.azertyuiop.tp2;

public class Adresse {

    public String numero;
    public String rue;
    public String codePostal;
    public String ville;
    public String pays;

    public Adresse(String numero, String rue, String codePostal, String ville, String pays) {
        this.numero = numero;
        this.rue = rue;
        this.codePostal = codePostal;
        this.ville = ville;
        this.pays = pays;
    }
}
